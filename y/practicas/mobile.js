$(document).ready(function() {
    // Función para ajustar la altura del modal según el contenido
    function ajustarAlturaModal() {
        const windowHeight = window.innerHeight;
        const modalContent = document.querySelector('.modal-body');
        const modalHeader = document.querySelector('.modal-header');
        const modalFooter = document.querySelector('.modal-footer');
        const modalPadding = 30; // Ajuste del padding para considerar los márgenes internos del modal

        if (modalHeader && modalFooter && modalContent) {
            const modalHeaderHeight = modalHeader.offsetHeight;
            const modalFooterHeight = modalFooter.offsetHeight;
            const modalContentHeight = windowHeight - (modalHeaderHeight + modalFooterHeight + modalPadding);

            modalContent.style.maxHeight = modalContentHeight + 'px';
            modalContent.style.overflowY = 'scroll'; // Cambiar a 'scroll' para siempre mostrar la barra de desplazamiento
        }
    }

    // Agregar un evento de escucha cuando el modal se muestre
    $('#usuariosModal').on('shown.bs.modal', ajustarAlturaModal);

    // Agregar un evento de escucha cuando la ventana cambie de tamaño
    window.addEventListener('resize', ajustarAlturaModal);
});
